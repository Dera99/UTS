    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        // TODO add your handling code here:
        int row = table.getSelectedRow();
        String click = (table.getModel().getValueAt(row,0).toString());
        try {
        String view = "SELECT * FROM data_barang WHERE `Kode Barang` = '"+click+"'";
        pst = conn.prepareStatement(view);
        rs=pst.executeQuery();
        if (rs.next()){
            String view1 = rs.getString("Kode Barang");
            kb.setText(view1);
            String view2 = rs.getString("Nama Barang");
            nb.setText(view2);
            String view3 = rs.getString("Model");
            md.setText(view3);
            String view4 = rs.getString("Jenis");
            jenis.setSelectedItem(view4);
            String view5 = rs.getString("Kadar");
            kadar.setSelectedItem(view5);
            String view6 = rs.getString("Berat");
            berat.setText(view6);
            pst.close();
        }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    
    }//GEN-LAST:event_tableMouseClicked

