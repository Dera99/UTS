    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        try{
        String insert = "INSERT INTO data_barang (`Kode Barang`, `Nama Barang`, `Model`, `Jenis`, `Kadar`, `Berat`) VALUES (?,?,?,?,?,?)";
        pst = conn.prepareStatement(insert);
        pst.setString(1, kb.getText());
        pst.setString(2, nb.getText());
        pst.setString(3, md.getText());
        String vj = jenis.getSelectedItem().toString();
        pst.setString(4, vj);
        String vk = kadar.getSelectedItem().toString();
        pst.setString(5, vk);
        pst.setString(6, berat.getText());
       if (kb.getText().equals("")){
           JOptionPane.showMessageDialog(null, "Kode Barang Tidak Boleh Kosong");
       }else{
        pst.execute();
        data_barang();
        JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
        pst.close();
       }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }                                           
    }//GEN-LAST:event_saveActionPerformed

